saldo = 200
saque = 200

print('Saldo é igual a saque:', saldo == saque)
print('Saldo é diferente a saque:', saldo != saque)
print('Saldo é maior que saque:', saldo > saque)
print('Saldo é maior ou igual a saque:', saldo >= saque)
print('Saldo é menor a saque:', saldo < saque)
print('Saldo é menor ou igual que saque:', saldo <= saque)
