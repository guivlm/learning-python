nome = "Guilherme Vinicio Leite Monteiro"

# considera a posição do início
print(nome[0])

# considera o fim da primeira string na quantidade x de caracteres
print(nome[-2])

# retorna os 9 primeiros caracteres
print(nome[:9])

# retorna a partir do número de caracteres especificado
print(nome[10:])

# retorna o texto entre os intervalos especificados
print(nome[10:18])

# especifica um step pegando de duas em duas letras no intervalo especificado
print(nome[10:16:2])

# retorna a string completa
print(nome[:])

# retorna a string invertida
print(nome[::-1])
